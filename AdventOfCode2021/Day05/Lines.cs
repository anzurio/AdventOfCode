namespace AdventOfCode2021.Day05
{
    internal class Lines
    {
        public int X1;
        public int Y1;
        public int X2;
        public int Y2;

        public Lines(int x1, int y1, int x2, int y2)
        {
            this.X1 = x1;
            this.Y1 = y1;
            this.X2 = x2;
            this.Y2 = y2;
        }
    }
}